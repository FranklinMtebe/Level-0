function celsiusToFahrenheit(celsius) {
    const fahrenheit = celsius * (9 / 5) + 32;
    return fahrenheit;
    
  }
  celsiusToFahrenheit(30);
  function fahrenheitToCelsius(Fahrenheit) {
    const Celsius = (5 / 9) * (Fahrenheit - 32);
    return Celsius;
    
  }
  fahrenheitToCelsius(20);